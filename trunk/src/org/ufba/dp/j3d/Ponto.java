package org.ufba.dp.j3d;

/**
 *
 * @author Helder Santana
 */
public class Ponto {
    private int x;
    private int y;
    private int z;

    public Ponto(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }
}